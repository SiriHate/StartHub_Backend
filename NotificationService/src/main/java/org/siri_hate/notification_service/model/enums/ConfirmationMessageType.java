package org.siri_hate.notification_service.model.enums;

/**
 * This enum represents the types of confirmation messages that can be sent.
 * It can be either a registration confirmation or a change password confirmation.
 */
public enum ConfirmationMessageType {

    /**
     * Represents a registration confirmation message.
     */
    REGISTRATION_CONFIRMATION,

    /**
     * Represents a change password confirmation message.
     */
    CHANGE_PASSWORD_CONFIRMATION

}